#include <iostream>

int main() {
    std::cout << "Olá mundo! Número: \t" << 42 << "\n";
    std::cout << "Quebra de linha -> \t" << std::endl;
    std::cout << "Fração: \t" << (float)5 / 8 << std::endl;
    std::cout << "Número grande: \t" << (double)7000 * 7000 * 7000 << std::endl;
    std::cout << "Estou aprendendo a programar em C++!" << std::endl;

    return 0;
}